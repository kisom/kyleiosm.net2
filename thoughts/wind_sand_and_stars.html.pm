◊h2{Thoughts on Wind, Sand, and Stars}

◊p{Antoine de Saint-Exupéry's 
◊link["https://www.goodreads.com/book/show/8837.Wind_Sand_and_Stars"]{◊strong{Wind, Sand, and Stars}}

(the original title in French being ◊strong{Terre des hommes}) is one
of my favourite books. I came across this book while reading another;
one of the characters in the novel

◊link["https://www.goodreads.com/book/show/17333223-the-goldfinch"]{◊strong{The Goldfinch}}

gives a copy to another character. On a whim, I decided to see if it
was a real book, and ended up buying right then. As I was going
through my Goodreads account, I found some of the quotes I saved about
it and it got me thinking about it.}

◊p{My favourite quote is this:}

◊blockquote{◊|oq|You have chosen not to be perturbed by great
problems, having trouble enough to forget your own fate as man. You
are not the dweller upon an errant planet and do not ask yourself
questions to which there are no answers. You are a petty bourgeois of
Toulouse. Nobody grasped you by the shoulder while there was still
time. Now the clay of which you were shaped has dried and hardened,
and naught in you will ever awaken the sleeping musician, the poet,
the astronomer that possibly inhabited you in the beginning.◊|cq|}

◊p{There are two experiences in my life that I think caused this book to
particularly resonate with me.}

◊blockquote{◊|oq|Thus is the earth at once a desert and a paradise,
rich in secret hidden gardens, gardens inaccessible, but to which the
craft leads us ever back, one day or another.◊|cq|}

◊p{First, in high school, I went through flight school far enough to
do a solo flight. This remains one of the highlights of my life: every
morning, I got up early, and in the crisp morning air prepared my
plane for flight. I had always wanted to be a pilot (but a lazy
prevented me from doing so professionally), and this was actually
living out my dream—and it was everything I wanted it to be. The
burden of responsibility at 16 or 17 of having an aircraft under your
command with no flight instructor to take over if things went south
left a significant impression on me.}

◊p{This aviation experience meant I could visualise, could
◊strong{feel} the experiences that St.-Exupéry described; I could
imagine the hum of the engines, the long periods of doing nothing but
checking your instruments occasionally or checking your course.}

◊blockquote{◊|oq|yet we were infinitely poor. Wind, sand, and
stars. The austerity of Trappists. But on this badly lighted cloth, a
handful of men who possessed nothing in the world but their memories
were sharing invisible riches.◊|cq|}

◊p{The other is the year that I spent in Africa; particularly, I spent
eight months mostly in Djibouti, with the occasional trip into
Ethiopia or Tanzania. The most striking experience during this time
was a ten day desert survival and warfare course conducted by the
French military. I distinctly remember being on a march in the middle
of the night, the desert stretching endlessly out in all directions
while the wind sighed as it meandered aimlessly over the parched
ground. The course was exceptionally challenging, with triple digit
temperatures during the day. More than a few hardened French marines
and US soldiers failed to make it through the course due to injury or
heat casualty. Several had such severe cases of heat stroke that they
had to be medivac'd out by helicopter. We had only what we could carry
on our backs, and kept that down to the minimum required. Every
kilogram adds up when you're marching ten or twenty kilometres per
day, with tactical movements interspersed and the occasional ambush or
assault.}

◊p{St.-Exupéry's treatment of war, of the bonds of comradeship, and
survival is at once haunting and poetic; filled throughout are his
thought-provoking ruminations. This book is due for a re-read... once
I work through a bit more of the stack.}
